<p><font color="red">UPDATE May 15, 2017: The web-based emulator has been ported/moved.</font><font> Here is its new home: </font><a href="http://www.dtss.org/dtss/">http://www.dtss.org/dtss/</a> From the site info blurb: "This web-based emulator has two components: a java-based application that emulates the GE-235 <i>hardware</i> in minute detail, and an asp.net/C# application that generally emulates the DN-30 <i>executive</i>. The two emulators communicate via sockets: one to emulate the Computer Interface Unit (CIU) that linked the DN-30 and GE-235 computers, and another to emulate the Disc Storage Unit (DSU) that was shared between the two computers."

<p><a href="#download">Simulator</a> available for Mac and Windows.

<p>June 2007 Reunion Photos <a href="reunion07.php">here</a> and <a href="dtss-reunion-2007">here.</a>

<p><b>The <a href="http://www.kemenyskids.com/forums/">forums</a> are still functional, if you tried to sign up and your account was never activated, please email me at the dot bates at gmail dot com and I will make you an account.</b> We had some spam issues which we've taken care of, and if you email me, I WILL make you an account. Thanks for your patience.

<p><hr> 


<p>A small group of "friends of DTSS" have started a project to recreate the first DTSS (Phase I), which ran from 1964 to sometime in 1967. This system ran on two computers, a GE-235 to execute programs (such as ones written in BASIC) and a GE DN-30 (Datanet-30) to handle communications (to and from teletypes) and to schedule the execution of programs on the 235. This ingenious two-computer design was created by Tom Kurtz and John Kemeny. 

<p>At the DCTS (Phase II) de-commisioning ceremony several of us got together and decided to attempt to recreate the original system. Our plan is to create emulators for each of the two computers, and use them to run the original software. The emulators will need a decent graphical interface so there is a fair amount of ancillary code that needs to be written. 

<p>The biggest problem we faced during the first year was to find listings of the original software. Some of us had a listing or two, and a few of us had the original computer manuals, but no one had a listing of BASIC. After about a year of searching, Steve Hobbs determined that George Friend, an ex-GE employee, had a box of old listings in his garage, one of which was Basic.

<p>Steve took the listings for the key software components and the manuals, and scanned them on a CD in several formats, including pdf.  These scans are available from this web site.  (He considered using OCR software, but it didn't work well enough to be practical. How do you tell a fuzzy capital "Oh" from a "Zero"?  The result would have had to have been proofread carefully, letter by letter.)  The alternative was to transcribe the source code, letter by letter, digit by digit.  This has now been done for the four principal software components. 

<ul>
<li>Basic
<li>Algol
<li>235 Executive
<li>DN-30 Executive
</ul>

(Tom Kurtz transcribed BASIC and Algol, John McGeachie did the 235 Exec, and Ron Martin the DN-30 Exec.)

<p>The next step was to "assemble" the source code.  Tom Kurtz wrote an Assembler (in True BASIC) and Ron Martin did the same for the DN-30 (in Excel.)  The assemblers then produced a replica of the original listing, the octal output of which could then be carefully proofread against the octal part of the original listing.

<p>Tom Kurtz then wrote an emulator for the GE-235 hardware, again using True BASIC.  This he used to test both the BASIC and Algol systems.  He faked the functionality of the DN-30.  His simulation accepts only one user at a time.

<p>
