<h2>The Current Status of the Project</h2>

<a href="index.php#download">Simulator</a> available for Mac and Windows.

<p>We have listings for the D-30 exec, the 235 exec, the Algol compiler, Algol run-time, and BASIC compiler/run-time/teach. These listing have been scanned and the images are available on the web page (is this true?). Ron Martin has transcribed the D-30 exec listing into a text file of source card images; John McGeachie has transcribed the 235 exec listing into a text file of source card images; and Tom Kurtz has transcribed the Algol compiler, Algol run-time and BASIC listings into text files. Tom Kurtz has written a GE-235 simulator good enough to test BASIC and Algol. Ron Martin has volunteered to write a D-30 simulator and John McGeachie has volunteered to write a more complete GE-235 simulator. 

<p>Our BASIC listing represents a version of BASIC maintained by GE.  It bears a date of February 1965. However, the assembly listing itself is not dated, so it is impossible to tell when it went into service. It includes the following: the INPUT statement; the additions by Keith Bellairs to locate named-variable storage at the top of lower memory; most of the coding for TEACH, although it contains several coding anomalies that would have prevented TEACH from running.  It is missing later features such as the MAT statements.  And, it could not have run John Kemeny's FTBALL*** program, which was written in the late fall of 1965.

We have a few more listings that have not yet been scanned nor transcribed, including time-sharing LISP and a batch version of WIZ. However, we are still looking for additional listings. We particularly would like to find: (1) the EDIT system, (2) contents of the LIBBAS, LIBALG, and TEACH libraries, (3) the TSAP system, and (4) background BASIC.

<p>It would also be interesting to find listings of some of the other software that is part of the system: Dartmouth GAP and D-30 GAP, SYMMAINT, time-sharing Fortran, time-sharing LAFFF (notes below), time-sharing DIP (who can remember what DIP was), the utility program that loaded the D-30 exec onto the disk, the D-30 boot paper tape. 

<p>NOTE: Dave Pearson has an answer to the query about LAFF: "I don't remember who coded it, but I remember that it was LAFFF, not LAFF -- Language to Aid Financial Fact Finders." 

<p>ADD'L NOTE: Nancy Broadhead also writes: "LAFFF was written by Dot Bower, who was not a Tuck student but was Tuck's research support person.  Her husband, Tuck prof Dick Bower, was presumably the inspiration for the project. Some years later, CRIII was added to the portfolio.  I don't remember how that acronym translates."

